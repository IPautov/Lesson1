#!/usr/bin/python3

a = float(input('Введите первое число: '))
b = float(input('Введите второе число: '))

print(f'{a}+{b} = {a + b}')
print(f'{a}*{b} = {a * b}')
print(f'{a}-{b} = {a - b}')
print(f'{b}-{a} = {b - a}')
print(f'{a}/{b} = {a / b }')
print(f'{b}/{a} = {b / a }')
print(f'{a}^{b} = {a ** b}')
print(f'{b}^{a} = {b ** a}')
print(f'{a}//{b} = {int(a // b)}')
print(f'{b}//{a} = {int(b // a)}')
print(f'{a}%{b} = {a % b}')
print(f'{b}%{a} = {b % a}')

